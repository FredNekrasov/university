# Задан вес в граммах. Определить вес в тоннах и килограммах.
grams = float(input("Введите вес в граммах: "))
tons = grams / 1_000_000  # 1 тонна = 1,000,000 грамм
print(f"Вес в тоннах: {round(tons, 4)} т")
kilograms = grams / 1_000  # 1 килограмм = 1,000 грамм
print(f"Вес в килограммах: {round(kilograms, 2)} кг")